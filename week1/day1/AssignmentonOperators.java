package week1.day1;

public class AssignmentonOperators {
	
	public static void main(String[] args) {
		
		int a =20; //Assignment operator
		int b =30;
		
		System.out.println(a+b); //Arithmetic operator
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		System.out.println(a==b); //comparison operator
		System.out.println(a!=b);
		
		System.out.println(a>b); //logical operator
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		
		System.out.println(a>b && a<b);
		System.out.println(a>b || a<b); 
		
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(b++);
		System.out.println(++b);
	}

}
